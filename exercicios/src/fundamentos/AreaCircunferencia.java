package fundamentos;

public class AreaCircunferencia {
	
	public static void main(String[] args) {
		
		System.out.println(2 + 3);
		System.out.println("--------------------------");
		
		int raio = 3;
		double raio2 = 3.4;
		final double PI = 3.14159; //final transforma valor em constante.
		
		System.out.println(PI * raio * raio);
		
		System.out.println("--------------------------");
		
		double area = PI * raio2 * raio2;
		
		System.out.println(area);
		
		System.out.println("--------------------------");
	}

}