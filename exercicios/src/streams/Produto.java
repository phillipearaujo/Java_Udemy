package streams;

public class Produto {

	final String nome;
	double preco;
	double desconto;
	final boolean freteGratis;
	
	public Produto(String nome, double preco, double desconto, boolean freteGratis) {
		this.nome = nome;
		this.preco = preco;
		this.desconto = desconto;
		this.freteGratis = freteGratis;
	}
	
	
	
	
}
